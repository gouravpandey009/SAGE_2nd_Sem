//SINGLY LINKED LIST
struct Node {
    int data;
    Node* next;
};

//DOUBLY LINKED LIST
struct Node {
    int data;
    Node* next;
    Node* prev;
};

//CIRCULAR LINKED LIST
struct Node {
    int data;
    Node* next;
};

//DOUBLY CIRCULAR LINKED LIST
struct Node {
    int data;
    Node* next;
    Node* prev;
};
